<p><strong>Biosafety issues under mandate of the institution:</strong></p>
<ul>
<li>Surveillance, control and prevention of foodborne diseases/illnesses,</li>
<li>Sampling of GMO food for analysis,</li>
<li>Issuance of import and export certificates for GMO food,</li>
<li>Inspection of GM food and food establishments.</li>
</ul>
<p><strong>GM products and regulatory activities handled under the mandate of the institution:</strong></p>
<ul>
<li>GM food meant for human consumption including food aid and locally produced and marketed, imported and exported GMO food.</li>
</ul>
<p><strong>Point of interaction/interaction with other agencies:</strong></p>
<ul>
<li>Member of NBC,</li>
<li>Secretariat of National Food Safety Coordination Committee,</li>
<li>Member of National Codex Committee,</li>
<li>Member of several Technical Committees on development of food standards,</li>
<li>The Department collaborates with the Director Veterinary Services in ensuring safety of food of animal origin.</li>
</ul>
<p><strong>Detection and screening of GMOs and GM products at the ports of entry:</strong></p>
<ul>
<li>The Department does screening of documentation of GMO food and GMO food aid in accordance with NBC procedures.</li>
</ul>
<p><strong>Food safety assessments:</strong></p>
<ul>
<li>The Department Participates in safety assessment of GM foods</li>
</ul>
<p><strong>Release and Post Environmental Release Monitoring:</strong></p>
<ul>
<li style="list-style-type: none;">
<ul>
<li>Surveillance of GM foods after placement on the market to ascertain human safety upon consumption. This includes monitoring of any consumer health concerns.</li>
</ul>
</li>
</ul>
