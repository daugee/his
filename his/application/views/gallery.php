<h3>Role of the Division of Food Safety and Quality Control, Ministry of Public Health &amp; Sanitation.</h3>
<p><strong>Introduction</strong></p>
<p>The Division of Food safety and quality control is one of the core divisions within the Department of Environmental Health and Sanitation. The division is mandated to ensure safety of food meant for human consumption and protection of the consumer against foodborne diseases/illnesses. The division strives to ensure confidence in the food safety and integrity of the food supply in order to protect the health of the consumers. Consumers expect protection from hazards occurring along the entire food chain; from the primary producer through to the consumer (farm/water-to-table continuum). The division has a fundamental role to in ensuring that food derived from modern biotechnology applications or techniques meet biosafety requirements.</p>
<p>Biosafety is a concept that refers to measures put in place to mitigate or protect human health and the environment from possible adverse effects of the products of modern biotechnology. The Cartagena Protocol on Biosafety provides a comprehensive and holistic regime designed to ensure that the development, handling, transport and use of products of modern biotechnology are undertaken in a manner that maximizes benefits while preventing or reducing risks to the environment and human health. The Protocol is a subsidiary agreement to the UN Convention on Biological Diversity (CBD).</p>
<p>Kenya signed the Biosafety Protocol in 2000 and fulfilled the ratification requirements in 2003. One of the key obligations expected from the Parties to the Protocol is promotion and facilitation of public awareness, education and participation in biosafety activities as stipulated in article 23.</p>
<p>The National Council for Science and Technology (NCST) under the Ministry of Higher Education, Science and Technology is the designated competent authority that handles all matters pertaining to biosafety in Kenya. The National Biosafety Committee (NBC) is the technical arm of the Council charged with the mandates of overseeing coordination and implementation of biosafety issues. The multiple roles of NBC include receiving, reviewing and making approval or rejection decisions on applications to introduce biotechnology products for research or commercial purposes into the country. The NBC platform is a broad one with multi-stakeholder representation. The Kenya Bureau of Standards (KEBS), the National Environment Management Authority (NEMA), the Department of Veterinary Services (DVS), the Kenya Plant Health Inspectorate Services (KEPHIS), the Division of Food Safety and Quality Control, and the Pest Control Products Board (PCPB) are among the key biosafety regulatory agencies represented on the NBC</p>
